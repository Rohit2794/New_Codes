package Base;

public class Addition {

	static void main()
	{
		int n1 =10;
		int n2= 20;
		
		int n3 = n1+n2;
		
	System.out.println(n3);
		
	}
	
	static void subs()
	{
		int n1 =10;
		int n2= 20;
		
		int n3 = n1-n2;
		
	System.out.println(n3);
		
	}
	
	static void multply()
	{
		int n1 =10;
		int n2= 20;
		
		int n3 = n1*n2;
		
	System.out.println(n3);
		
	}
	
	
	static void divide()
	{
		int n1 =40;
		int n2= 20;
		
		int n3 = n1/n2; /// will alawys give the quotation
		
	System.out.println(n3);
	
	}
	
	
	
	static void modulus()
	{
		int n1 =44;
		int n2= 20;
	
		int n4 = n1%n2;
	System.out.println(n4);
	}
	
	public static void main(String[] args) {
		
  main();
  subs();
  multply();
  divide();
  modulus();
	}
	
	}
